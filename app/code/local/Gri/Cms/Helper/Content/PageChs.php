                            <li>保养这类皮质最佳的方式就是“预先的防护”，先喷上麂皮的防水以及防尘喷雾剂</li>
                            <li>若沾到水,则趁鞋子上的水珠尚未渗透时,再喷一次 </li>
                            <li>若鞋面上有灰尘时,须在麂皮干燥状态则使用小塑料刷或是橡胶轻轻的梳理麂皮的短毛并除去上面的灰尘</li>
                            <li>如要对麂皮的材质进行保养或喷雾，必须两只一起并全部进行，否则会产生色差。</li>
                        </ul>
                    </li>
                    <li class="usage"><span>布质</span>
                        <ul>
                            <li>保持干燥:水分会破坏布料的形状并且使原来的颜色退去,如果不小心弄湿鞋子,必需以白色的面纸塞住鞋内并且放置风干，千万不可以用热风吹或是受到太阳曝晒。</li>
                            <li>防尘以及防水喷剂:这可以有效的隔离脏污以及水渍,延长鞋子的寿命, 新鞋第一次穿著前, 花几分钟喷上防护剂, 可以拥有它更长的时间。</li>
                            <li>经常清理:不可以将这类型的鞋子放进机器里洗涤,需使用干净的毛巾以及特殊的清洁剂, 尽量不要单独清理某个区块, 这样将会造成整个鞋子颜色不均匀。</li>
                        </ul>
                    </li>
                    <li class="usage"><span>漆皮</span>
                        <ul>
                            <li>漆皮可以使用专用的漆皮保养剂然后以干净的棉布擦拭即可</li>
                            <li>漆皮在收藏时注意与其他颜色的货品分开放置，以免被污染到</li>
                            <li>如果浅色的漆皮货品已经被污染，请购买专用清洁剂</li>
                        </ul>
                    </li>
                </ul>
                <div class="class">包包的清洁与保养</div>
                <ul>
                    <li class="usage"><span>日常保养方法</span>
                        <ul>
                            <li>正确的使用习惯，可延长皮具使用寿命。包袋一定要避免与硬、锐物发生碰撞、刮擦，以免表面皮革刮花。包内避免放过重物品，以免起皱、变形。 </li>
                            <li>不要每天使用同一个包，用过一段时间后让它休息一、两天取出内置物于另一包里。</li>
