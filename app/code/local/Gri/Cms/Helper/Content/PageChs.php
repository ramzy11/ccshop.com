            <h4>鞋子清洁和保养:</h4>
            <div class="class">一般材质的清洁方法：</div>
            <p class="cms-text">保养鞋子的基本用品有鞋刷、清洁油、鞋油、布、防水喷雾等物。最好使用马毛的鞋刷，并至少拥有两支——大的刷鞋子整体，小的刷细部。</p>
                <ul>
                    <li>用大鞋刷轻刷整个鞋子，看看鞋沿的凸出部分有无沾染污垢。</li>
                    <li>鞋沿或接缝等凹凸部分，用小鞋刷刷掉污垢细尘。</li>
                    <li>松开鞋带，再用小鞋刷刷清从外侧不易见到的污垢。</li>
                </ul>
            <div class="class">日常保养方法:</div>
                <ul>
                    <li>新买的皮鞋，若在穿用之前涂上一层护鞋油，擦亮，并喷上防水雾，就能使其长久保持光洁。</li>
                    <li>鞋子受潮及摩擦而产生的掉色，均属正常现象。</li>
                    <li>平日小心穿用，是鞋子保养的第一步。鞋子损坏多从后跟开始。因此，可多利用鞋拔子穿鞋。穿鞋时应先松开鞋带再使用鞋拔。</li>
                    <li>防止鞋子变形，塞入鞋楦效果最佳。脚形、走法、体温和湿气、雨水等因素均会导致鞋子走样，尤其是出脚汗或遭雨淋时，最好用鞋楦子固定鞋形。</li>
                    <li>鞋子和人一样需要休息，不要每天都穿同一双鞋去上班，建议至少准备二三双鞋轮流替换。避免在以下不适当情况下穿着，雨天、运动、骑车、湿滑地面、不平路面、雪地、沙地等，延长鞋子使用寿命。</li>
                    <li>穿皮鞋不宜作剧烈运动，否则皮鞋会变形走样。定期查看天皮(鞋跟着地面的添加层), 发现有深度磨损应及时更换。</li>
                    <li>定期清洁鞋子，用半湿的布轻轻擦拭即可；切勿用刷子猛刷。</li>
                    <li>鞋子切忌爆晒,受潮,火烤,水洗,锐物碰撞和接触化学溶剂, 酸，碱，油等易腐蚀物。以免造成损坏。</li>
                    <li>淋湿后的保养法：皮革遇水会变形，因此雨天不宜穿着。如遇上雨，皮鞋被雨浸泡，先利用吸水性佳的布料吸去鞋子表面的水分。再擦一次油，放入鞋楦，放在干燥通风处晾干。</li>
                </ul>
