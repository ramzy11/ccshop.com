                            <li>当鞋子不小心沾到脏污时,即刻以有溶解性的皮质清洁剂清理,防油的溶剂也适用.</li>
                            <li>最后喷上防水雾。防水雾不仅能使鞋子防止水的侵袭，同时还具有不易沾染污垢的效果。</li>
                            <li>防水雾干后，再将鞋楦置入鞋中。</li>
                        </ul>
